#!/usr/bin/env python3
"""Build DC-L3 antagonism reasoning dataset for LLM benchmark.

Generates 200 records for reasoning about WHY drug combinations are antagonistic.
Only selects pairs with consensus_class='antagonistic' and high antagonism_fraction.

Split: fewshot 30 + test 170

Usage:
    PYTHONPATH=src python scripts_dc/build_dc_l3_dataset.py
"""

from __future__ import annotations

import argparse
import logging
import sys
from pathlib import Path

import numpy as np

logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")
logger = logging.getLogger(__name__)

PROJECT_ROOT = Path(__file__).resolve().parent.parent
OUTPUT_DIR = PROJECT_ROOT / "exports" / "dc_llm"


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Build DC-L3 reasoning dataset.")
    parser.add_argument("--db", type=Path, default=PROJECT_ROOT / "data" / "negbiodb_dc.db")
    parser.add_argument("--output", type=Path, default=OUTPUT_DIR / "dc_l3_dataset.jsonl")
    parser.add_argument("--n-records", type=int, default=200)
    parser.add_argument("--seed", type=int, default=42)
    args = parser.parse_args(argv)

    from negbiodb_dc.llm_dataset import (
        apply_max_per_drug,
        assign_splits,
        construct_l3_context,
        load_dc_candidate_pool,
        write_dataset_metadata,
        write_jsonl,
    )

    rng = np.random.RandomState(args.seed)

    from negbiodb_dc.dc_db import get_connection
    conn = get_connection(args.db)
    try:
        df = load_dc_candidate_pool(conn, min_confidence="bronze", consensus_class="antagonistic")
    finally:
        conn.close()

    # Require target info and multiple cell lines for rich reasoning
    df = df[df["drug_a_targets"].notna() & df["drug_b_targets"].notna()]
    df = df[df["num_cell_lines"] >= 2]

    # Prefer higher antagonism fraction
    df = df.sort_values("antagonism_fraction", ascending=False)

    if len(df) > args.n_records * 2:
        df = df.head(args.n_records * 2)

    df = apply_max_per_drug(df, max_per_drug=8, rng=rng)

    if len(df) > args.n_records:
        df = df.sample(n=args.n_records, random_state=rng).reset_index(drop=True)

    df = assign_splits(df, fewshot_size=30, val_size=0, seed=args.seed)

    records = []
    for i, (_, row) in enumerate(df.iterrows()):
        rec = {
            "question_id": f"DCL3-{i:04d}",
            "task": "dc-l3",
            "split": row["split"],
            "context_text": construct_l3_context(row),
            "gold_reasoning": None,  # Generated by judge, not pre-existing
            "metadata": {
                "pair_id": int(row["pair_id"]),
                "drug_a": row["drug_a_name"],
                "drug_b": row["drug_b_name"],
                "median_zip": float(row["median_zip"]) if row.get("median_zip") is not None else None,
                "antagonism_fraction": float(row["antagonism_fraction"]),
            },
        }
        records.append(rec)

    write_jsonl(records, args.output)
    write_dataset_metadata(
        args.output.with_suffix(".meta.json"), "dc-l3",
        len(records), dict(df["split"].value_counts()),
        seed=args.seed,
    )

    logger.info("DC-L3 dataset built: %d records", len(records))
    return 0


if __name__ == "__main__":
    sys.exit(main())
